            <h2 class="left-center">What is Discreet Drop?</h2>
            <p>Discreet Drop is a Buffalo-based startup founded in 2017 by Kevin Skompinski, serving to create a way to make sure that every item people order can arrive to them as discreet as possible. Privacy has become a pseudo-luxury in today's digital age, with any information being available just a simple search way. We've identifed this gap in privacy protection with all the information and identifiable logos a shipped box provides to anyone who may see it. Even merchants who advertise discreet plain-box shipping, still provide a return label with a name and address where one quick Google search would reveal what's in any package. We've realized that if you're not handling your package at all times, your privacy is vulnerable on any shipment.</p>
