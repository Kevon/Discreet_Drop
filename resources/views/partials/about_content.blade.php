            <h2 class="left-center">Yes, but what do you do?</h2>
            <p>With this in mind, we've created Discreet Drop as a solution to cover up those privacy issues, literally. We serve as a shipment processor, where people can ship their packages to us via a unique address, from any online store. We then double-box the package to cover up any external logos, return labels, packing slips, and even holes; anything that can act as identifying information and can be used to signal what's in the box. Finally we ship the plain-box out to the customer so it will arrive completely discreet, and nobody but the customer would be able to figure out what's in the package. Easy.<br><br>
            With Discreet Drop, packages will arrive with no logos or branding to attract attention from anyone, reveal the contents of the box, and won't have any identifying information that can be used to figure out is in a package. With Discreet Drop, only the customer will ever know what is in their packages.<br><br>
            Our <a href="/how-it-works">how it works page</a> has more information about the Discreet Drop process and explains, well, how it works! Discreet Drop is the only way to guarantee 100% discreet shipping on any package, from any seller.</p>
