            <h2 class="left-center">What is Discreet Drop?</h2>
            <p>Discreet Drop is a Buffalo-based startup founded in 2017 by Kevin Skompinski, serving to create a way to make sure that every item people order can arrive to them as discreet as possible. Privacy has become a pseudo-luxury in today's digital age, with any information being available just a simple search way. We've identifed this gap in privacy protection with all the information and identifiable logos a shipped box provides to anyone who may see it. Logos, packing slips, and customs forms are obvious clues as to the contents of a package. Even merchants who advertise discreet shipping still have a return label on their packages which lists the seller's name and address; both of which can be quickly Googled to reveal what's in any package. We've realized that if you're not handling your package at all times, your privacy is vulnerable on any shipment. Only the recepient should know what's in their package.</p>
